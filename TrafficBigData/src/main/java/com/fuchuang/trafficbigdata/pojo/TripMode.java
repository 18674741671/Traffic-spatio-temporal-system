package com.fuchuang.trafficbigdata.pojo;

public class TripMode {

    private String longitude;
    private String latitude;
    private String mode;
    private String mode_name;
    private String mode_num ;

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getMode() {
        return mode;
    }

    public void setMode(String mode) {
        this.mode = mode;
    }

    public String getMode_name() {
        return mode_name;
    }

    public void setMode_name(String mode_name) {
        this.mode_name = mode_name;
    }

    public String getMode_num() {
        return mode_num;
    }

    public void setMode_num(String mode_num) {
        this.mode_num = mode_num;
    }
}
